class calculate:
    def plus(self,a,b):
        print(a+b)
    def minus(self,a,b):
        print(a-b)
    def umn(self,a,b):
        print(a*b)
    def razdel(self,a,b):
        print(a/b)
    def stepen(self,a,b):
        print(a**b)
    
cl=calculate()
cl.plus(5,6)
cl.minus(5,6)
cl.umn(5,6)
cl.razdel(5,6)
cl.stepen(5,6)
